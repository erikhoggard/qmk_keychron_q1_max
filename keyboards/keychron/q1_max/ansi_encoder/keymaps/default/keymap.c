bool process_record_user(uint16_t keycode, keyrecord_t *record) {
    if (!process_record_keychron_common(keycode, record)) {
        return false;
    }

    switch (keycode) {
        case FN_WIN_1:
            if (record->event.pressed) {
                layer_move(MAC_BASE); // Switch to MAC_BASE layer
            }
            return false;

        case FN_WIN_2:
            if (record->event.pressed) {
                layer_move(MAC_FN); // Switch to MAC_FN layer
            }
            return false;

        case FN_WIN_3:
            if (record->event.pressed) {
                layer_move(WIN_BASE); // Switch to WIN_BASE layer
            }
            return false;

        case FN_WIN_4:
            if (record->event.pressed) {
                layer_move(WIN_FN); // Switch to WIN_FN layer
            }
            return false;
    }

    return true;
}
